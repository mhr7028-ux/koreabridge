// Pronunciation evaluation helper
function evaluateSpeechAccuracy(speechResult) {
    const target = aebSentences[selectedSentenceIndex].korean.replace(/[.,\/#!$%\^&\*;:{}=\-_`~()?]/g,"").replace(/\s/g, "");
    const recited = speechResult.replace(/[.,\/#!$%\^&\*;:{}=\-_`~()?]/g,"").replace(/\s/g, "");
    
    // Simple edit distance ratio comparison
    let matchCount = 0;
    const minLength = Math.min(target.length, recited.length);
    for (let i = 0; i < minLength; i++) {
        if (target[i] === recited[i]) matchCount++;
    }
    
    const accuracy = Math.round((matchCount / Math.max(target.length, 1)) * 100);
    const scoreVal = Math.min(accuracy + 20, 100); // add booster for natural variations
    
    let remark = "Good attempt! Let's practice once more. Try adjusting the speed.";
    if (scoreVal >= 90) remark = "🏆 Perfect Recitation! Excellent pronunciation, native-like accent!";
    else if (scoreVal >= 75) remark = "🔥 Great Job! Almost perfect. Just a tiny vowel adjustment needed.";

    document.getElementById("aeb-analysis-result").innerHTML = `
        <strong style="color: ${scoreVal >= 80 ? 'var(--color-success)' : 'var(--color-gold)'}">
            AI Score: ${scoreVal} / 100
        </strong>
        <p style="margin-top: 6px; font-size: 13px; color: var(--text-secondary);">${remark}</p>
    `;
}

