// Increment likes on story
function likeStory(storyId) {
    const story = communityStories.find(s => s.id === storyId);
    if (!story) return;

    if (story.liked) {
        story.likes--;
        story.liked = false;
    } else {
        story.likes++;
        story.liked = true;
        story.views += 5; // Simulating organic views boost
    }

    renderStories();
}

// Helpers
function extractYoutubeId(url) {
    const regExp = /^.*(youtu.be\/|v\/|u\/\w\/|embed\/|watch\?v=|\&v=)([^#\&\?]*).*/;
    const match = url.match(regExp);
    return (match && match[2].length === 11) ? match[2] : null;
}
