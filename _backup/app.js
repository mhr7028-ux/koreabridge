// Text-to-speech engine wrapper
function speakText(text, langCode, rateVal = 1.0) {
    if ('speechSynthesis' in window) {
        window.speechSynthesis.cancel(); // stop previous speech
        const utterance = new SpeechSynthesisUtterance(text);
        utterance.lang = langCode === 'en' ? 'en-US' : langCode === 'es' ? 'es-ES' : langCode === 'jp' ? 'ja-JP' : langCode === 'cn' ? 'zh-CN' : langCode === 'id' ? 'id-ID' : langCode === 'my' ? 'ms-MY' : langCode;
        utterance.rate = rateVal;
        window.speechSynthesis.speak(utterance);
    }
}

